	// ** Magic Power Belt +1
	public static ItemStack magicPowerBeltLvl1() {
		
		// ** Variables
		int level = 1;
		int magicPowerBonus = 1;
		
		// ** Item
		ItemStack item = new ItemStack(Material.DIAMOND_HOE, 1);
		ItemMeta meta = item.getItemMeta();
		
		// ** Attributes and Name
		meta.setDisplayName(ChatColor.DARK_PURPLE + "" + ChatColor.BOLD + "Magic Power Belt (+" + level + ")");
		meta.setUnbreakable(true);
		meta.setCustomModelData(1000003);
		meta.addItemFlags(ItemFlag.HIDE_UNBREAKABLE);
		meta.addItemFlags(ItemFlag.HIDE_ENCHANTS);
		meta.addItemFlags(ItemFlag.HIDE_ATTRIBUTES);
	
		// ** Lore
		List<String> lore = new ArrayList<>();
		lore.add(ChatColor.DARK_PURPLE + "(Upgradeable Item)");
		lore.add(ChatColor.GRAY + "Belt");
		lore.add(null);
		lore.add(ChatColor.GREEN + "Magic Power Bonus: " + magicPowerBonus);
		
		// ** Set Lore and Meta
		meta.setLore(lore);
		item.setItemMeta(meta);
	
		return item;
	}
	
